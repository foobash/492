# Functions

# So we now look at Mean Absolute Error.

MAE <- function(actual, predicted) {
  mean(abs(actual - predicted))
}

MAPE <- function(y, yhat) {
  mean(abs((y - yhat)/y))
}

con2back <- function(y) {
  y*(max - min) + min #min/max defined from data.  
}

